end_year = 2026

rows = []

for _, r in df.iterrows():
    start = r["Start"]
    country = r["country"]

    # recurring event
    if pd.notna(r["Occurs every"]):
        years = range(start, end_year + 1)
    else:
        years = [start]

    for y in years:
        rows.append({
            "year": y,
            "country": country
        })

year_country_df = pd.DataFrame(rows)

year_country_counts = (
    year_country_df
    .groupby(["year", "country"])
    .size()
    .reset_index(name="num_events")
)

year_country_counts

events_summary = pd.DataFrame({
    "before_2010": year_country_counts[year_country_counts["year"] < 2010]
        .groupby("country")["num_events"].sum(),
    "from_2010": year_country_counts[year_country_counts["year"] >= 2010]
        .groupby("country")["num_events"].sum(),
    "all_time": year_country_counts
        .groupby("country")["num_events"].sum(),
}).fillna(0).astype(int)

