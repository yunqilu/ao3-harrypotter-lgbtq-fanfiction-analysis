import pandas as pd
import requests
from bs4 import BeautifulSoup
import re

url = "https://en.wikipedia.org/wiki/List_of_LGBTQ_events"

headers = {
    "User-Agent": "Mozilla/5.0"
}

response = requests.get(url, headers=headers)
response.raise_for_status()

soup = BeautifulSoup(response.text, "html.parser")

# Find all wikitable tables
tables = soup.find_all("table", class_="wikitable")

all_rows = []

for table_idx, table in enumerate(tables):
    # 1. Find continent (nearest h2)
    continent_tag = table.find_previous("h2")
    if continent_tag:
        continent = continent_tag.get_text(" ", strip=True).replace("[edit]", "").strip()
    else:
        continent = None

    # 2. Find table header
    first_tr = table.find("tr")
    if first_tr is None:
        continue

    ths = first_tr.find_all("th")
    colnames = [th.get_text(" ", strip=True).replace("[edit]", "").strip() for th in ths]

    # Only keep the first 5 columns
    if len(colnames) >= 5:
        colnames = colnames[:5]
    else:
        colnames = ["LGBT event", "Location", "Official website", "Occurs every", "Start"]

    current_country = None

    # 3. Iterate through each row
    for tr in table.find_all("tr")[1:]:
        tds = tr.find_all("td")

        if len(tds) == 0:
            continue

        # 4. Country grouping row: only one td with colspan
        if len(tds) == 1 and tds[0].has_attr("colspan"):
            h3 = tds[0].find("h3")
            if h3:
                current_country = h3.get_text(" ", strip=True)
            else:
                current_country = tds[0].get_text(" ", strip=True)

            current_country = current_country.replace("[edit]", "").strip()
            continue

        # 5. Regular event row: strictly keep first 5 cells, preserve empty values
        row = []
        for i in range(5):
            if i < len(tds):
                text = tds[i].get_text(" ", strip=True)
                text = re.sub(r"\[\d+\]", "", text).strip()
                row.append(text if text != "" else None)
            else:
                row.append(None)

        record = dict(zip(colnames, row))
        record["continent"] = continent
        record["country"] = current_country
        record["table_index"] = table_idx

        all_rows.append(record)

# Convert to DataFrame
df = pd.DataFrame(all_rows)

